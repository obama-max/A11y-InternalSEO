    <p class="pt-4 fs-6">	
    3 -Основни HTML Елементи:

Основни HTML елементи включват:
Заглавия (&lt;h1>, &lt;h2>, ..., &lt;h6>) за структуриране на текста.
Абзаци (&lt;p>) за текстово съдържание.
Връзки (&lt;a>) за създаване на хипервръзки към други страници.
Списъци (&lt;ul>, &lt;ol>, &lt;li>) за представяне на списъци от елементи.
Изображения (&lt;img>) за вмъкване на графични файлове.
Важно е да се отбележи, че HTML има и семантични елементи като &lt;header>, &lt;nav>, &lt;main> и &lt;footer>, които се използват за подобряване на структурата и семантиката на документа.
    </p>	

    <p class="pt-4 fs-6">	
    4 - Форми в HTML и Входни Данни от Потребителя:

HTML формите ( &lt;form>) са от съществено значение за събирането на входни данни от потребителите, като текст, избори и изпращане на информация.
Научете се как се използват различни елементи за форми, като текстови полета ( &lt;input type="text">), радио бутони ( &lt;input type="radio">), отметки ( &lt;input type="checkbox">) и падащи списъци ( &lt;select>).
Разберете формалите атрибути, като "action" и "method", които определят къде и как се обработват данните от формата.
Изучете как да извършвате валидация на формите и как да гарантирате точност и сигурност на данните, предоставени от потребителите.
Открийте как да създадете бутони и да управлявате изпращането на формулярите с  &lt;button> и  &lt;input type="submit">.
    </p>	

	
    
  

