    <p class="pt-4 fs-4">   
    HTML (Hypertext Markup Language) е стандартен език за маркиране, който се използва за създаване на уеб страници. Този език позволява на уеб разработчиците да структурират и форматират информацията, която браузърите изобразяват на екрана на потребителите. Важно е да разберете някои основни концепции:
    </p>

    
	<p class="pt-4 fs-6">	
     1 - HTML Документ и Основен Синтаксис:

HTML документът е текстов файл, който съдържа код написан на HTML езика. Този код съдържа HTML елементи и тагове.
HTML елементите са съставни части от уеб страницата, които се определят с тагове. Примери на тагове са &lt;p> за абзаци, &lt;h1> за заглавия, и &lt;a> за връзки.
Основният синтаксис включва отварящ таг, съдържание и затварящ таг, като например &lt;p>Това е текст&lt;/p>. Отварящите и затварящите тагове трябва да съвпадат.
    </p>	

    <p class="pt-4 fs-6">	
    2 -Структура на HTML Документа:

HTML документът започва с декларация за типа на документа (DOCTYPE), която определя версията на HTML, която се използва. Например: &lt;!DOCTYPE html>.
&lt;html> елементът е кореновият елемент на HTML документа и определя началото и края на уеб страницата.
&lt;head> секцията съдържа метаданни за документа, като заглавие, кодировка и линкове към външни стилове и скриптове.
&lt;body> секцията съдържа видимото съдържание на уеб страницата, включително текст, изображения, и други медийни елементи.
    </p>	


