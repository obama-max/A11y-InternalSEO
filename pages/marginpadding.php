<h2 class="pt-4 fs-4">
Margin и Padding 
</h2>

	<p class="pt-4 fs-6">
   
    Margin и Padding ни помагат и дават способността да правим промени към нашите елементи и тяхното положение. Margin се използва за промяната на разстоянието на елемента спрямо други елементи в сайта. Padding се използва за промяната на разстоянието на елемента спрямо себе си и неговата граница.	</p>

    <img src="./assets/images/CSS1.png" style="height: 300px; width: 600px;" alt="snimka lul">

	<p class="pt-4 fs-4">	
        Маргини:
    </p>	

	<div class="row fs-6">
		<div class="col-6">
			<p>
				<ul>
					<li>
                    Маргините са пространството извън границата на елемента. Те създават разстояние между елемента и неговите съседни елементи.
                    </li>	

					<li class="mt-2">
                    Можете да зададете маргини за всички страни (горе, дясно, долу, ляво) индивидуално или в съкратен формат.
                    </li>

                    <li class="mt-2">
                    Отрицателни маргини могат да се използват, за да се прекриват елементи или да ги приближат.
                   </li>

                   <li class="mt-2">
                   Стойността auto може да се използва, за да центрирате хоризонтално блокови елементи в техния родителски контейнер.
                   </li>		
                </ul>	 
			</p>     
		</div>

		<div class="col-6">
            <p class="fs-4">	
                Падинги:
             </p>
				<ul>
					<li>
                    Падингите са пространството вътре в границата на елемента. Те създават разстояние между съдържанието на елемента и неговата граница.
                    </li>

					<li class="mt-2">
                    Можете да зададете падинги за всички страни индивидуално или в съкратен формат.
                    </li>

                    <li class="mt-2">
                    Падингът влияе на размера на областта за съдържание на елемента.
                    </li>				
                </ul>	
			</p>     
		</div>
	</div>
    
