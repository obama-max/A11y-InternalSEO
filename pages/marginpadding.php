    <p class="pt-4 fs-6">
    Помнете, че маргините и падингите могат да влияят на оформлението и разстоянието на елементите в рамките на контейнер. Общата ширина на елемент се изчислява като добавите неговата ширина, падинга и границата. Подобно на това, общата височина включва височината, падинга и границата.

Като използвате маргини и падинги ефективно, можете да управлявате бялото пространство около и вътре в елементите, постигайки желаното визуално разстояние и оформление за вашата уеб страница
    </p>