<h2 class="pt-4 fs-4">
Блок елементи
</h2>

	<p class="pt-4 fs-6">
   
    В HTML елементите се категоризират в два основни типа: блокови елементи (block-level elements) и инлайн елементи (inline elements). Блоковите елементи създават блокова кутия, което означава, че те започват на нов ред и заемат цялата налична ширина в рамките на своя родителски контейнер. Тези елементи обикновено се използват за структуриране и организиране на съдържанието на уеб страница.

	</p>

