		Повече информация за точните правила според принципите <a href="https://www.w3.org/WAI/standards-guidelines/wcag/">WCAG</a>, <a href="https://www.w3.org/WAI/standards-guidelines/atag/">ATAG</a>, <a href="https://www.w3.org/WAI/standards-guidelines/uaag/">UAAG</a>; може да се намери в сайта на <a href="https://www.w3.org/WAI/fundamentals/accessibility-principles/#perceivable">W3C Percievable</a> частта. Когато отворите подкатегорията "accessibility requirements" изизат всички правила според принципите за достъпност.
	</p>