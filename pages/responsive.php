    <h1 class="pt-4 fs-3 d-flex justify-content-center" tabindex="0">
		Респонзивен дизайн:
	</h1>
	
    <p class="fs-6" tabindex="0">
    Отзивен уеб дизайн (responsive web design) е подход към уеб разработката, който има за цел да направи уеб страниците да се показват добре на различни устройства и размери на екраните. Този подход гарантира, че уебсайтовете са потребителски приятни и визуално привлекателни, независимо дали се разглеждат на настолен компютър, лаптоп, таблет, смартфон или на други устройства с интернет връзка. Отзивният дизайн е от съществено значение в съвременната цифрова среда, където потребителите достъпват уебсайтове чрез разнообразие от устройства с различни размери на екрани и резолюции.
    </p>

