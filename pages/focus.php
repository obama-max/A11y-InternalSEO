
	<h2 class="pt-4 fs-4">
		Управление на фокуса:
	</h2>
	
	<p class="pt-4 fs-6">	
		Осигуряването на правилни стилове на фокуса и навигационен поток помага на потребителите да разберат текущото си местоположение на страницата и подобрява достъпността с клавиатура. В практиката фокус е начин да кажем на сайта при използване на устройства различни от мишкита как потребителя да навигира нашият сайт.  При настикането на Tab ние можем спокойно да се движим през сайта, на различни елементи като h1, buttons , a или елементи които имат атрибута <a href="https://developer.mozilla.org/en-US/docs/Web/HTML/Global_attributes/tabindex">tabindex</a>. Предоставянето на достъпност чрез клавиатура помага на потребителите с увреждания да използват уеб сайтовете си с равни възможности.
	</p> 
	
	<p class="pt-4 fs-4">	
