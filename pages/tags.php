<h2 class="pt-4 fs-4">
Етикетите в HTML
</h2>

		

    <p class="pt-4 fs-6">   
    Етикетите в HTML са като ключови думи, които определят как уеб браузърът ще форматира и показва съдържанието. С помощта на етикетите уеб браузърът може да различава между HTML съдържание и обикновено съдържание. Етикетите в HTML съдържат три основни части: отварящ етикет, съдържание и затварящ етикет. Но някои етикети в HTML са незатворени етикети. Когато уеб браузър прочита HTML документ, той го чете отгоре надолу и отляво надясно1.
Например, етикетът &lt;p> се използва за дефиниране на параграф в HTML документ. Той има отварящ етикет &lt;p>, съдържание (текст или други елементи) и затварящ етикет &lt;/p>. Всичко между отварящия и затварящия етикет ще бъде форматирано като параграф от уеб браузъра.
    </p>

    <pre>
    <code>
    &lt;p>Това е пример за параграф в HTML.&lt;/p>
    </code>
    </pre>

    <p class="pt-4 fs-6">   
    Някои етикети в HTML нямат затварящ етикет, защото не съдържат никакво съдържание. Тези етикети се наричат самозатварящи се или празни етикети. Например, етикетът &lt;br> се използва за дефиниране на нов ред в HTML документ. Той няма затварящ етикет, защото не съдържа никакво съдържание. Той просто указва на уеб браузъра да премине на нов ред.
    </p>	

<pre>
<code>
&lt;p>Това е пример за нов ред в HTML.&lt;br> <br>Това е друг ред.&lt;/p>
</code>
</pre>

<p class="pt-4 fs-6">   
Етикетите в HTML могат да имат атрибути, които предоставят допълнителна информация за елемента. Атрибутите обикновено се поставят в отварящия етикет и имат две части: име и стойност. Името на атрибута определя каква информация ще бъде предоставена, а стойността на атрибута определя конкретната информация. Например, атрибутът href се използва за дефиниране на хипервръзка към друг уеб адрес. Името на атрибута е href, а стойността на атрибута е уеб адресът, към който трябва да сочи хипервръзката.
    </p>	

    
<pre>
<code>
&lt;a href="https://www.bing.com">Това е пример за хипервръзка към Bing.&lt;/a></code>
</pre>


