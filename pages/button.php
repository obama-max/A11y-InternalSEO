		<div class="col-12">
			<p>
				<ul>
					<li>
                        display: inline-block;- Това свойство прави бутона да се държи като инлайн елемент, но позволява на същото време да има свойства на блоков елемент, като ширина и височина.                    
                    </li>	

					<li class="mt-2">
                        padding: 10px 20px;- Добавя падинг (разстояние от вътрешната страна) вътре в бутона. Първата стойност (10px) представлява падинга горе и долу, докато втората стойност (20px) представлява падинга наляво и надясно.                    
                    </li>

                    <li class="mt-2">
                        font-size: 16px;- Задава размера на шрифта на текста на бутона на 16 пиксела.                    
                    </li>

                    <li class="mt-2">
                        background-color: #007bff;- Задава цвета на задния план на бутона на син цвят (#007bff). Можете да използвате всяка валидна стойност за цвят тук.                    
                    </li>

                    <li class="mt-2">
                        color: white;- Задава цвета на текста в бутона на бяло, за да създаде добра контрастност със синия заден план.                    
                    </li>

                    <li class="mt-2">
                        border: none;- Премахва границата около бутона. Това е един начин да стилизирате границата на бутона.                    
                    </li>

                    <li class="mt-2">
                        border-radius: 5px;- Добавя закръглени ъгли на бутона, задавайки радиус на границата от 5 пиксела.                    
                    </li>	

                    <li class="mt-2">
                        cursor: pointer;-Променя вида на курсора в показалеца на мишката, когато се навеждаме върху бутона, което показва, че бутона може да бъде кликнат.                    
                    </li>
                </ul>	 
			</p>     
		</div>
