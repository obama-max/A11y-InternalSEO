        HTML Елемент за Бутон:
    </p>	

    <p class="pt-4 fs-6">   
        HTML елементът "button" се използва за създаване на кликваеми бутони на уеб страници. Бутоните могат да се използват за различни цели, като изпращане на формуляри, стартиране на действия или навигация към различни страници. Ето как можете да използвате елемента "button" в HTML:
    </p>

    
	<p class="pt-4 fs-4">	
        Често използвани атрибути:
    </p>	

	<div class="row fs-6">
		<div class="col-6">
			<p>
				<ul>
					<li>
                        type: Определя поведението на бутона. Стойностите могат да бъдат "button" (по подразбиране), "submit" или "reset". Например: <button type="submit">Изпращане</button>.
                    </li>	

					<li class="mt-2">
                        name и value: Използват се, когато бутона е част от формуляр. Атрибутът name определя името на бутона, а атрибутът value задава стойността, която ще бъде изпратена на сървъра.
                    </li>

                    <li class="mt-2">
                        disabled: Деактивира бутона, като го прави некликлив.
                    </li>

                    <li class="mt-2">
                        onclick: Определя JavaScript функция, която да се изпълни при щракване на бутона.
                    </li>		
                </ul>	 
			</p>     
		</div>
	</div>
    
