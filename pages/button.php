    <p class="pt-4 fs-6">
        Псевдокласът :hover се използва, за да приложи стилове, когато мишката се наведе върху бутона. В този случай променяме цвета на задния план на бутона на по-тъмно син (#0056b3), за да се даде визуален отзив при прехвърляне.
        Псевдокласът :active се използва, за да стилизира бутона, когато бъде натиснат. В този случай променяме цвета на задния план на бутона на още по-тъмно син (#003a80), за да покажем, че бутона се натиска.
        Тези CSS правила обединяват се, за да създадат визуално привлекателен и интерактивен елемент бутон. Можете да промените стойностите, за да се адаптират към вашите дизайнерски предпочитания и да интегрирате стиловете в по-големия си проект.
    </p>

