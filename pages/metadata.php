Метаданни и Други Елементи в Заглавната Секция на HTML Документа
</h2>

		

    <p class="pt-4 fs-6">   
    HTML документите се състоят не само от видимото съдържание, което потребителят вижда на екрана, но и от метаданни и други важни информации, които браузърите и търсачките използват за различни цели. Тази информация се намира в заглавната секция (&lt;head>) на HTML документа и играе ключова роля за оптималната работа на уеб страницата.
    </p>

   

    <p class="pt-4 fs-6">   
    Метаданни (Meta Tags):

    Декларация за Кодировка (Charset): С метатега &lt;meta charset="UTF-8"> се установява, че документът използва кодировката UTF-8, която     позволява представянето на различни символи и езици.
    Заглавие на Уеб Страницата (Title): Метатегът &lt;title> съдържа заглавието на уеб страницата и се показва в заглавието на браузъра или    като заглавие на таба в браузъра.
    Метатег за Описание (Description): Метатегът &lt;meta name="description" content="Текст за описание на страницата"> предоставя кратко  описание на уеб страницата, което се използва в резултатите на търсачките.
    Метатег за Ключови Думи (Keywords): Въпреки че не се използва толкова често в съвременната уеб разработка, метатегът &lt;meta  name="keywords" content="ключови, думи, за, страницата"> съдържа ключови думи, свързани със съдържанието на страницата.    
</p>	



<p class="pt-4 fs-6">   
Линкове към Стилове и Скриптове:

Линкове към Външни Стилове (CSS): С помощта на метатега  &lt;link rel="stylesheet" href="styles.css"> се връзват външни стилове, които управляват визуалния дизайн на страницата.
Линкове към JavaScript Файлове: Метатегът  &lt;script src="script.js"> &lt;/script> се използва за връзка с външни JavaScript файлове, които предоставят функционалност и интерактивност на страницата.    
</p>	

    
<p class="pt-4 fs-6">   
Линкове и Други Ресурси:

Линкове към Свързани Страници: Метатегът  &lt;link rel="canonical" href="https://www.example.com/page"> се използва за указване на предпочитаната версия на страницата, когато има няколко URL адреса, свързани с едно съдържание.
Линкове към Икони (Favicons): Метатегът  &lt;link rel="icon" href="favicon.ico" type="image/x-icon"> указва икона, която се показва в таба на браузъра и в бутоните за добавяне към "Избрани" или "Любими".
Метатег за Автори (Author): Метатегът  &lt;meta name="author" content="Име на автора"> указва името на автора на уеб страницата.    
</p>	

