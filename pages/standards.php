	<h2 class="pt-4 fs-4" tabindex="0">
		Ръководство за достъпност на уеб съдържание (WCAG):
	</h2>

	<p class="pt-4 fs-6" tabindex="0">	
		<a href="https://www.w3.org/WAI/standards-guidelines/wcag/">WCAG</a> е набор от насоки, публикувани от Инициативата за уеб достъпност (<a href="https://www.w3.org/WAI/">WAI</a>) на Световния уеб консорциум (<a href="https://www.w3.org/">W3C</a>). Тя предоставя комплексна рамка за увереност, че уеб съдържанието е възприемливо, изпълнимо, разбираемо и надеждно за всички потребители. <a href="https://www.w3.org/WAI/standards-guidelines/wcag/">WCAG</a> е организирана в три нива на съответствие: A, AA и AAA, като най-широко приетото ниво е AA.
	</p>  

	<p class="pt-4 fs-5 fw-medium" tabindex="0">	
