	<h1 class="pt-4 fs-3 d-flex justify-content-center" tabindex="0">
		Стандарти на уеб достъпността:
	</h1>

	<h3 class="pt-4 fs-4 fw-semibold" tabindex="0">
		Уеб достъпността се базира на няколко компонента, които работят заедно. Някои от тях включват:
	</h3>

	<p class="pt-1 fs-6" tabindex="0">
		<span class="fw-semibold">Уеб съдържание</span> - отнася се до всяка част от уеб сайт, включително текст, изображения, форми и мултимедийни елементи, както и css, скриптове, приложения и други подобни.
	</p>

	<p class="pt-1 fs-6" tabindex="0">
		<span class="fw-semibold">Потребителски помощен софтуер</span> - софтуер, който хората използват за достъп до уеб съдържание, включително графични браузъри за настолни компютри, гласови браузъри, мобилни телефонни браузъри, мултимедийни плеъри, добавки и някои помощни технологии.
	</p>

	<p class="pt-1 fs-6" tabindex="0">
		<span class="fw-semibold">Инструменти за създаване на съдържание</span> - софтуер или услуги, които хората използват за производство на уеб съдържание, включително редактори на код, инструменти за конвертиране на документи, системи за управление на съдържание, блогове, скриптове за бази данни и други инструменти.
	</p>

	<p class="pt-4 fs-6" tabindex="0">
		Стандартите играят важна роля в определянето на изискванията за достъпност за всеки от тези компоненти. Някои изисквания за достъпност са лесни за изпълнение, но разбирането на основите за това как хората с увреждания използват уеб пространството помага за по-ефективна и ефективна имплементация. Някои аспекти на достъпността изискват по-технически умения или напреднало познание за начина, по който хората използват уеб пространството.
	</p>

	<h3 class="pt-4 fs-5 fw-medium" tabindex="0">
		Инициативата за уеб достъпност на W3C (Интернет консорциумът за уеб стандарти) предоставя комплект от насоки, които се признават международно като стандарт за уеб достъпност. Тези включват:
	</h3>

	<div class="row fs-6">
		<div class="col-6">
			<p>
				<ul tabindex="0">
					<li tabindex="0">
						Ръководство за достъпност на уеб съдържание (<a href="https://www.w3.org/WAI/standards-guidelines/wcag/">WCAG</a>):
					</li>

					<li class="mt-2" tabindex="0">
						Настройки за достъпност на потребителски агенти (<a href="https://www.w3.org/WAI/standards-guidelines/uaag/">UAAG</a>):
					</li>

					<li class="mt-2" tabindex="0"> 
						Настройки за достъпност на инструменти за създаване на съдържание (<a href="https://www.w3.org/WAI/standards-guidelines/atag/">ATAG</a>):
					</li>

					<li class="mt-2" tabindex="0">
