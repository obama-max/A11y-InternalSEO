	<h2 class="pt-4 fs-4" tabindex="0">
		Настройките за достъпност на потребителски агенти (User Agent Accessibility Guidelines - UAAG):
	</h2>

	
	<p class="pt-4 fs-6" tabindex="0">
		<a href="">UAAG</a> са част от усилията на <a href="">W3C</a> за подобряване на уеб достъпността и създаването на стандарти, които да подпомагат хората с увреждания при използването на различни видове софтуер, наречени "потребителски агенти". Тези насоки се фокусират върху начините, по които софтуерните приложения, използвани от хората за достъп до уеб съдържание, могат да бъдат по-достъпни и удобни за потребителите с увреждания.
	</p>

	<p class="pt-4 fs-6" tabindex="0">
		Потребителските агенти включват разнообразен софтуер, като например настолни браузъри, гласови браузъри, мобилни браузъри, мултимедийни плеъри и дори асистивни технологии като екраниращи програми, които помагат на хората с увреждания да използват компютри и уеб съдържание. <a href="">UAAG</a> цели да подобри достъпността на тези софтуерни приложения, като предоставя насоки и стандарти, които разработчиците може да следват.
	</p>

	<p class="pt-4 fs-5 fw-medium" tabindex="0">	
		Някои от ключовите аспекти, които UAAG адресира, включват:
	</p>

	<div class="row fs-6">
		<div class="col-6">
			<p>
				<ul tabindex="0">
					<li tabindex="0">
						<span class="fw-semibold">Поддръжка на стандарти за достъпност:</span> UAAG насърчава софтуерните приложения да поддържат и следват утвърдените стандарти за уеб достъпност, като например Web Content Accessibility Guidelines (<a href="">WCAG</a>).
					</li>

					<li class="mt-2" tabindex="0">
						<span class="fw-semibold">Навигация и взаимодействие:</span> Насоките препоръчват, че потребителските агенти трябва да предоставят достъпни и удобни методи за навигация и взаимодействие с уеб съдържание, включително възможности за управление с клавиатура, гласови команди и други алтернативни входни методи.
					</li>

					<li class="mt-2" tabindex="0"> 
						<span class="fw-semibold">Визуална и аудио информация:</span> <a href="https://www.w3.org/WAI/standards-guidelines/uaag/">UAAG</a> се стреми да осигури достъп до визуална и аудио информация чрез поддръжка на алтернативни представяния като текстови описания и аудио субтитри.
					</li>

					<li class="mt-2" tabindex="0">
						<span class="fw-semibold">Адаптивност и персонализация:</span> Стандартите насърчават софтуерните приложения да предоставят възможности за адаптивност и персонализация на интерфейса, които да отговарят на индивидуалните нужди на потребителите.
					</li>

					<li class="mt-2" tabindex="0">
						<span class="fw-semibold">Поддръжка на асистивни технологии:</span> <a href="https://www.w3.org/WAI/standards-guidelines/uaag/">UAAG</a> насърчава интеграцията на функционалности, които поддържат работата с асистивни технологии, като например екраниращи програми и четци на съдържание.
					</li>
				</ul>	 
			</p>     
		</div>
	</div> 

	<h2 class="pt-4 fs-4" tabindex="0">
	Настройките за достъпност на инструменти за създаване на съдържание (Authoring Tool Accessibility Guidelines - ATAG):
	</h2>

	<p class="pt-4 fs-6" tabindex="0"><a href="https://www.w3.org/WAI/standards-guidelines/atag/">ATAG</a> са също част от усилията на W3C за подобряване на уеб достъпността. Те се фокусират върху начините, по които софтуерните инструменти, използвани от автори и създатели на уеб съдържание, могат да бъдат по-достъпни и поддържаеми, за да се създава уеб съдържание, което е по-лесно за разбиране и използване от хора с увреждания.</p>

	<p class="fs-6" tabindex="0">Създаването на достъпно уеб съдържание е от съществено значение, и <a href="https://www.w3.org/WAI/standards-guidelines/atag/">ATAG</a> цели да предостави на разработчиците на инструменти насоки и стандарти, които да ги насърчат да създават софтуер с вградени функционалности за подпомагане на достъпността.</p>

	<div class="row">
		<div class="col-6">
			<p class="pt-4 fs-5 fw-medium" tabindex="0">
				Някои от ключовите аспекти, които ATAG адресира, включват:
			</p>

			<p class="pt-4 ">
				<ul tabindex="0">
					<li class="fs-6" tabindex="0">
						<span class="fw-semibold">Поддръжка на стандарти за достъпност:</span><a href="https://www.w3.org/WAI/standards-guidelines/atag/">ATAG</a> насърчава инструментите за създаване на съдържание да предоставят функционалности, които помагат на авторите да създават съдържание, съответстващо на утвърдените стандарти за уеб достъпност, като например Web Content Accessibility Guidelines (<a href="https://www.w3.org/WAI/standards-guidelines/wcag/">WCAG</a>).
					</li>

					<li class="mt-2 fs-6" tabindex="0">
						<span class="fw-semibold">Поддръжка на удобен интерфейс за достъпност:</span> <a href="https://www.w3.org/WAI/standards-guidelines/atag/">ATAG</a> насърчава създаването на интуитивен и достъпен потребителски интерфейс на инструментите, които да подпомагат авторите при създаването на достъпно съдържание.
					</li>

					<li class="mt-2 fs-6" tabindex="0">
						<span class="fw-semibold">Управление на алтернативни текстове и мултимедия:</span> Насоките препоръчват, че инструментите трябва да предоставят средства за добавяне на алтернативен текст за изображения и мултимедийни елементи, които са важни за разбирането на съдържанието от хора с увреждания.
					</li>

					<li class="mt-2 fs-6" tabindex="0">
						<span class="fw-semibold">Проверка на достъпността и подаване на съобщения за грешки:</span> <a href="https://www.w3.org/WAI/standards-guidelines/atag/">ATAG</a> подчертава значението на инструментите да предоставят възможности за автоматична проверка на достъпността и да информират авторите за евентуални нарушения.
					</li>

					<li class="mt-2 fs-6" tabindex="0">
						<span class="fw-semibold">Асистивни функции за авторите:</span> Насоките насърчават инструментите да предоставят помощни средства и функции за авторите, които им помагат да създават достъпно съдържание.
					</li>
				</ul>
			</p>
