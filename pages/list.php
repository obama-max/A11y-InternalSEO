Списък в HTML
</h2>

		

    <p class="pt-4 fs-6">   

    Списък в HTML е начин за организиране и показване на колекция от свързани елементи или информация в структуриран формат. Списъците се използват обикновено, за да представят информация по четим и организиран начин. В HTML има три основни вида списъци:

    </p>

   

    <p class="pt-4 fs-6">   

    Ключови атрибути на елемента &lt;form> включват:    

    </p>	


   
	<div class="col-12">
		<ul>
			<li class="fs-6">
                Неномерирани списъци (&lt;ul>): Неномерираният списък се използва, когато елементите в списъка нямат конкретна последователност или ред, и искате да ги покажете с точки или други маркери. Всеки елемент в неномерирания списък обикновено предшества точка. Този вид списък е подходящ за представяне на колекция от елементи без определен ред.
            </li>
			
			<li class="mt-2 fs-6">
                Списък с дефиниции (&lt;dl>): Списъкът с дефиниции се използва, за да дефинира термини и съответните им описания. Всеки термин е затворен в таг &lt;dt> (дефиниран термин), а всяко описание е затворено в таг &lt;dd> (дефинирано описание). Списъците с дефиниции често се използват за глосари, речници или обяснения.
			</li>
		</ul>
	</div>

