	<h2 class="pt-4 fs-4">
		ARIA (Accessible Rich Internet Applications):
	</h2>
	<p class="pt-4 fs-6">	
		<a href="https://www.w3.org/WAI/standards-guidelines/aria/">ARIA</a> е набор от атрибути, които могат да бъдат добавени към <a href="https://developer.mozilla.org/en-US/docs/Web/HTML">HTML</a> елементи, за да подобрят достъпността на динамичното и интерактивно съдържание. Той помага на помощните технологии да разберат целта и поведението на различните елементи. Чете се главно от помощните технологии за да можем да доставим по-добре и по-лесно преживяване на потребителите с недостатъци.
	</p>   
	
	<p class="pt-4 fs-4">	
		Ето някои ключови аспекти на достъпността чрез ARIA:
	</p>
	<div class="row fs-6">
		<div class="col-6">
			<p>
				<ul>
					<li>
						Роля на елементите
					</li>
					<li class="mt-2">
						Състояние на елементите
					</li>
					<li class="mt-2"> 
						Управление на фокуса
					</li>
				</ul>	 
			</p>     
		</div>

		<div class="col-6">
			<p>
				<ul>
					<li>
						Описание на съдържанието
					</li>
					<li class="mt-2">
						Достъпност на приложенията
					</li>
				</ul>	
			</p>     
		</div>
	</div>
	<p class="pt-4 fs-6">	
		Употребата на <a href="https://www.w3.org/WAI/standards-guidelines/aria/">ARIA</a> атрибути и роли е иновативен и важен начин за подобряване на достъпността на уеб сайтовете и приложенията. При разработка и проектиране на уеб сайтове, разработчиците трябва да имат предвид възможностите на <a href="https://www.w3.org/WAI/standards-guidelines/aria/">ARIA</a> и да го използват, където е необходимо, за да предоставят по-добра достъпност и удобство на потребителите с увреждания. Има поговорка че "Никоя <a href="https://www.w3.org/WAI/standards-guidelines/aria/">ARIA</a> не е по-добра от лошата <a href="https://www.w3.org/WAI/standards-guidelines/aria/">ARIA</a>." причината за това е че след голям анализ над милиони страници беше намерено че повече от 41% от тях имаха грешки. Трябва да се обърне сериозно внимание или ще се направят повече проблеми отколкото удобства.
