<h2 class="pt-4 fs-4">
 Контейнер
</h2>

	<p class="pt-4 fs-6">
   
    В CSS няма специфичен "container" (контейнер) елемент, но терминът "контейнер" се използва неформално, за да се отнася към блоков елемент, който обгръща и съдържа други елементи в определена секция на уеб страница. Обикновено този елемент се стилизира, за да осигури структура и оформление на съдържанието, което съдържа. Общи примери за елементи-контейнери включват елементи "div" или елементи със специфични имена на класове.


	</p>

	<p class="pt-4 fs-4">	
        Как контейнерите често се използват в CSS:
	</p>	

