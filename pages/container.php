                        Структурна роля: Контейнерите помагат да се структурира оформлението на уеб страница, като групират свързано съдържание заедно. Те могат да разделят страницата на секции, колони или блокове, което прави стилизирането и управлението на съдържанието по-лесно.					
                    </li>	

					<li class="mt-2">
                    Обхват на оформление: Контейнерите ви позволяват да приложите специфични правила за оформление към група от елементи. Можете да зададете клас или идентификатор на контейнер и след това да приложите CSS правила за този клас или идентификатор, което засяга всички елементи в контейнера.					</li>             
				</ul>	 
			</p>     
