	<p class="pt-4 fs-6">
   
    CSS селекторите са шаблони, използвани за избор и оформление на HTML елементи в уеб страница. Те ви позволяват да насочите специфични елементи или групи от елементи въз основа на техните атрибути, свойства и йерархия в HTML структурата. Ето подробен преглед на CSS селекторите заедно с примери:	</p>

