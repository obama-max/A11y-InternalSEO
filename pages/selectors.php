    <p class="pt-4 fs-6">
    В този пример:

Селекторът * задава margin и padding на 0 за всички елементи на страницата.
Селекторът h1 променя размера на шрифта на елементите h1.
Селекторът .button оформя елементите с клас button.
Селекторът #header оформя елемент с идентификатор header.
Селекторът a:hover променя цвета на връзките при навлизане с мишка.
Селекторът p::first-line прави първия ред на параграфите удебелен.
