<h2 class="pt-4 fs-4 text-center">
JavaScript: Ръководство за Начинаещи
</h2>
	

    <p class="pt-4 fs-6">   
    JavaScript е високо ниво, интерпретируем, програмен език, който се използва в основно за уеб разработка. Той позволява добавянето на динамично поведение и интерактивност към уеб страниците. JavaScript се използва както на страната на клиента (в браузъра), така и на страната на сървъра (чрез Node.js) и предоставя множество инструменти и възможности за създаване на разнообразни уеб сайтове. Най-известния програмен език е JavaScript.
    </p>


<p class="col-12 text-center fs-4 my-3">Какви са важните неща за JavaScript?</p>

<div class="col-12 row my-3">
    <div class="col-6">
    <ul class="col-12">
        <li class="pt-4 fs-6 col-12">   
            Къде се ползва JavaScript?: <br>

            JavaScript се използва не само за уеб разработка, но и за разработка на десктоп и мобилни приложения. Той е част от     семейството на езиците ECMAScript и намира приложение извън уеб браузърите в среди като Node.js.        
        </li>

        <li class="pt-4 fs-6 col-12">   
            Синтаксис: <br>

            В синтаксиса на JavaScript има много специфични детайли, включително разнообразни оператори, работа с масиви и обекти,  манипулация на стрингове и повече. За пример разликата между "const", "let" и "var" е голяма.
        </li>

        <li class="pt-4 fs-6 col-12">   
            Интерактивност и DOM: <br>

            Динамичните уеб страниците се постигат чрез манипулация на DOM. JavaScript може да добавя, премахва и променя HTML и    CSS елементи. За по-голяма детайлност, разработчиците използват методи и селектори, като getElementById, querySelector,    и други да карат различни елементи да бъдат променени или махнати от страницата.
        </li>

        <li class="pt-4 fs-6 col-12">   
        Инструменти и Ресурси: <br>
    
        Разработчиците изучават инструменти като интегрирани разработвателски среди (IDE) и допълнителни инструменти за дебъгване и     профилиране. Освен това, изучаването на ресурси като MDN (Mozilla Developer Network) и Stack Overflow е от съществено   значение за развитието.
    </li>
        </div>
    </ul>
    <div class="col-6">

    <li class="pt-4 fs-6 col-12">   
        Събития: <br>

        Събития в HTML представляват “неща”, които се случват на елементите.
        В JavaScript вие може да прихванете и да обработите тези събития. Примери за HTML събития са страницата е завършила своето зареждане, променена е стойността в текстово поле, потребителят е натиснал някой бутонДаден елемент е избран и др. .
    </li>

    <li class="pt-4 fs-6 12">   
        Асинхронно Програмиране: <br>

        Асинхронното програмиране в JavaScript се използва, когато искате да извършвате операции, които отнемат време (като извличане на данни от сървър, четене на файлове или изчакване на потребителски вход), без да блокирате изпълнението на останалата част от кода. Вместо да чакате операцията да завърши, асинхронният код позволява на приложението да продължи да работи, докато операцията се изпълнява отзад. Това се извършва с помощта на "обещания" че нещо се извършва, след определено време или с "Callback" функции, които чакат някоя друга функции да се извърши преди те да се завършат.
    </li>

    <li class="pt-4 fs-6 col-12">   
        Библиотеки и Рамки: <br>

        В разширено изучаване на JavaScript, разработчиците изучават не само стандартния език, но и библиотеки и рамки като Redux,     Axios, и Express.js, които подпомагат улесняването на разработката. Най-често с времето хората сменят JavaScript с TypeScript.
    </li>    
    </ul>
