<h2 class="pt-4 fs-4">
Формите в HTML
</h2>

		

    <p class="pt-4 fs-6">   

    HTML елементът &lt;form> е основна част от уеб разработката и позволява на потребителите да въвеждат и изпращат данни. Той се използва за създаване на различни видове полета за въвеждане, като текстови полета, отметки и радио бутони, където потребителите могат да въвеждат информация. Когато потребителят изпраща формата, данните се изпращат на сървъра за обработка. Освен това, елементът  &lt;form> може да съдържа множество полета за въвеждане, бутони и други елементи на формуляра. Обикновено съдържа бутон за изпращане, който стартира изпращането на формата.
   </p>

   

    <p class="pt-4 fs-6">   

    Ключови атрибути на елемента &lt;form> включват:    

    </p>	


   
	<div class="col-12">
		<ul>
			<li class="fs-6">

            action: Указва URL адреса, където данните от формата ще бъдат изпратени след изпращане.

            </li>
			
			<li class="mt-2 fs-6">

            method: Определя HTTP метода (GET или POST), използван за изпращането на данните.

			</li>
			
			<li class="mt-2 fs-6">

            enctype: Указва как данните от формата се кодират, когато се изпращат към сървъра, обикновено за качване на файлове.

            </li>			
		</ul>
	</div>

